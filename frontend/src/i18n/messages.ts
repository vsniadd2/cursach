export type MessageKey = string;

const dictionaries: Record<AppLanguage, Record<string, unknown>> = { ru, en };

export function translate(language: AppLanguage, key: MessageKey): string {
  const parts = key.split('.');
  let node: unknown = dictionaries[language];
  for (const part of parts) {
    if (node == null || typeof node !== 'object') break;
    node = (node as Record<string, unknown>)[part];
  }
  if (typeof node === 'string') return node;
  // fallback to Russian
  let fallback: unknown = dictionaries.ru;
  for (const part of parts) {
    if (fallback == null || typeof fallback !== 'object') break;
    fallback = (fallback as Record<string, unknown>)[part];
  }
  return typeof fallback === 'string' ? fallback : key;
}

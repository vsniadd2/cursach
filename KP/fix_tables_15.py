def find_docx() -> str:
    for name in os.listdir(KP_DIR):
        if name.endswith(".docx") and not name.startswith("~") and not name.endswith(".bak"):
            return os.path.join(KP_DIR, name)
    raise FileNotFoundError("docx не найден")


def set_cell_text(cell, text: str) -> None:
    if not cell.paragraphs:
        cell.text = text
        return
    p = cell.paragraphs[0]
    if p.runs:
        p.runs[0].text = text
        for r in p.runs[1:]:
            r.text = ""
    else:
        p.add_run(text)
    for extra in cell.paragraphs[1:]:
        if extra.runs:
            extra.runs[0].text = ""
            for r in extra.runs[1:]:
                r.text = ""


def set_row(table, row_index: int, values: list[str]) -> None:
    row = table.rows[row_index]
    for ci, val in enumerate(values):
        if ci < len(row.cells):
            set_cell_text(row.cells[ci], val)


def delete_rows_from_end(table, keep_rows: int) -> None:
    while len(table.rows) > keep_rows:
        table._tbl.remove(table.rows[-1]._tr)


def clear_table(table) -> None:
    while len(table.rows) > 0:
        table._tbl.remove(table.rows[0]._tr)


def fill_checklist_table(table) -> None:
    clear_table(table)
    for row_data in CHECKLIST:
        cells = table.add_row().cells
        for ci, val in enumerate(row_data):
            if ci < len(cells):
                set_cell_text(cells[ci], val)


def build_testcase_rows() -> list[list[str]]:
    rows: list[list[str]] = []
    rows.append([ENV_ROW] + [""] * 5)
    rows.append(HEADER_ROW)
    for case_id, title, steps in TEST_CASES:
        for step in steps:
            rows.append([case_id, title, step[0], step[1], step[2], step[3]])
    return rows


def fill_testcases_table(table) -> None:
    rows = build_testcase_rows()
    clear_table(table)
    for row_data in rows:
        cells = table.add_row().cells
        for ci, val in enumerate(row_data):
            if ci < len(cells):
                set_cell_text(cells[ci], val)


def main() -> None:
    path = find_docx()
    doc = Document(path)
    fill_checklist_table(doc.tables[14])
    fill_testcases_table(doc.tables[15])
    out = path
    try:
        doc.save(path)
    except PermissionError:
        out = os.path.join(KP_DIR, "ПЗ_Родненков_КП26_15tests.docx")
        doc.save(out)
        print("ВНИМАНИЕ: основной файл открыт в Word. Сохранено в:", out)
    assert len(CHECKLIST) - 1 == 15
    assert len(TEST_CASES) == 15
    print("OK: чек-лист", len(CHECKLIST) - 1, "записей; тест-кейсов", len(TEST_CASES))
    print("Строк табл.14:", len(doc.tables[14].rows))
    print("Строк табл.15:", len(doc.tables[15].rows))


if __name__ == "__main__":
    main()

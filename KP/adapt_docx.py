KP_DIR = os.path.dirname(os.path.abspath(__file__))


def find_docx() -> str:
    for name in os.listdir(KP_DIR):
        if name.endswith(".docx") and not name.startswith("~") and "_adapted" not in name:
            return os.path.join(KP_DIR, name)
    raise FileNotFoundError("docx не найден в KP")


def iter_text_nodes(element):
    for t in element.iter(qn("w:t")):
        if t.text:
            yield t


def replace_in_document(doc: Document, replacements: list[tuple[str, str]]) -> None:
    """Замена подстрок во всех w:t с сохранением разметки."""
    reps = sorted(replacements, key=lambda x: -len(x[0]))
    for node in iter_text_nodes(doc.element.body):
        text = node.text
        changed = False
        for old, new in reps:
            if old in text:
                text = text.replace(old, new)
                changed = True
        if changed:
            node.text = text


def set_paragraph_text(paragraph, new_text: str) -> None:
    if not paragraph.runs:
        paragraph.add_run(new_text)
        return
    paragraph.runs[0].text = new_text
    for run in paragraph.runs[1:]:
        run.text = ""


def set_cell_text(cell, new_text: str) -> None:
    if not cell.paragraphs:
        cell.text = new_text
        return
    set_paragraph_text(cell.paragraphs[0], new_text)
    for p in cell.paragraphs[1:]:
        set_paragraph_text(p, "")


def replace_paragraph_if_match(doc: Document, old: str, new: str) -> int:
    n = 0
    for p in doc.paragraphs:
        if p.text.strip() == old.strip():
            set_paragraph_text(p, new)
            n += 1
    return n


def replace_table_rows(doc: Document, table_index: int, rows: list[list[str]]) -> None:
    table = doc.tables[table_index]
    for ri, row_data in enumerate(rows):
        if ri >= len(table.rows):
            break
        for ci, val in enumerate(row_data):
            if ci >= len(table.rows[ri].cells):
                break
            set_cell_text(table.rows[ri].cells[ci], val)



def apply_ui_tables(doc: Document) -> None:
    """Таблицы 4–13: краткие описания элементов CRM (сохраняем структуру строк)."""
    ui_replacements = [
        ("Логотип XPARK", "Логотип «Экспого»"),
        ("парковк", "клиент"),
        ("Парковк", "Клиент"),
        ("бронирован", "сделк"),
        ("Бронирован", "Сделк"),
        ("брони", "сделки"),
        ("QR", "карточк"),
        ("балл", "задач"),
        ("карт", "запис"),
        ("платеж", "настройк"),
        ("Leaflet", "списк"),
        ("XPARK", "Экспого"),
        ("Парковка без стресса", "Управление клиентами и продажами"),
        ("Войти через Google", "Дополнительный способ входа (отключён)"),
        ("PayPal", "дополнительный пункт"),
    ]
    for ti in range(4, min(14, len(doc.tables))):
        for row in doc.tables[ti].rows:
            for cell in row.cells:
                for p in cell.paragraphs:
                    t = p.text
                    for old, new in ui_replacements:
                        if old in t:
                            set_paragraph_text(p, t.replace(old, new))


def replace_structure_section(doc: Document) -> None:
    """Заменяет абзацы описания структуры после заголовка 2.5."""
    start = False
    paras = doc.paragraphs
    indices = []
    for i, p in enumerate(paras):
        if p.text.strip() == "В корневой папке проекта:":
            start = True
        if start:
            if p.text.strip().startswith("3. ТЕСТИРОВАНИЕ"):
                break
            if p.text.strip() and not p.text.strip().startswith("Рис."):
                indices.append(i)
    if not indices:
        return
    for idx, line in zip(indices, STRUCTURE_LINES):
        set_paragraph_text(paras[idx], line)


def main() -> None:
    src = find_docx()
    backup = src + ".bak"
    if not os.path.exists(backup):
        shutil.copy2(src, backup)

    doc = Document(src)

    for old, new in EXACT_PARAGRAPHS:
        if new:
            replace_paragraph_if_match(doc, old, new)
        else:
            for p in doc.paragraphs:
                if p.text.strip() == old.strip():
                    set_paragraph_text(p, "")

    for old, new in IO_PARAGRAPHS.items():
        replace_paragraph_if_match(doc, old, new)

    for old, new in GUIDE_REPLACEMENTS:
        replace_paragraph_if_match(doc, old, new)

    for old, new in MOCK_HEADERS.items():
        replace_paragraph_if_match(doc, old, new)

    for old, new in FIGURE_CAPTIONS.items():
        replace_paragraph_if_match(doc, old, new)

    for ti, rows in DB_TABLES.items():
        replace_table_rows(doc, ti, rows)

    if len(doc.tables) > 14:
        replace_table_rows(doc, 14, CHECKLIST_ROWS)

    replace_structure_section(doc)
    apply_ui_tables(doc)
    replace_in_document(doc, SUBSTRING_REPLACEMENTS)

    # Окружение тестирования в таблице 15
    env_old = "веб-приложение «Икс-парк»"
    env_new = "приложение «Экспого» (клиент Expo + API .NET + PostgreSQL)"
    if len(doc.tables) > 15:
        for row in doc.tables[15].rows:
            for cell in row.cells:
                if env_old in cell.text:
                    set_cell_text(cell, cell.text.replace(env_old, env_new))

    doc.save(src)
    print("Готово:", src)
    print("Резервная копия:", backup)


if __name__ == "__main__":
    main()
